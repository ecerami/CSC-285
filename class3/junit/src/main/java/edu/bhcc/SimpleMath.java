package edu.bhcc;

/**
 * Simple Math Class.
 */
public class SimpleMath {

    /**
     * Add Two Numbers.
     * @param a first number.
     * @param b second number.
     * @return sum of a and b.
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * Multiple Two Numbers.
     * @param a first number.
     * @param b second number.
     * @return a times b.
     */
    public int multiply(int a, int b) {
        return a * b;
    }
}
